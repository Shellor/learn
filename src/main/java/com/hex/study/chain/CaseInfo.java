package com.hex.study.chain;

/**
 * @author hui.zhu
 */
public class CaseInfo {
   private String caseNo;
   private String caseType;
   private String interbankType;
   private String transMarket;
   private String payAccountNo;
   private String recvAccountNo;
   private String payAccountName;
   private String recvAccountName;

    public String getCaseNo() {
        return caseNo;
    }

    public void setCaseNo(String caseNo) {
        this.caseNo = caseNo;
    }

    public String getCaseType() {
        return caseType;
    }

    public void setCaseType(String caseType) {
        this.caseType = caseType;
    }

    public String getInterbankType() {
        return interbankType;
    }

    public void setInterbankType(String interbankType) {
        this.interbankType = interbankType;
    }

    public String getTransMarket() {
        return transMarket;
    }

    public void setTransMarket(String transMarket) {
        this.transMarket = transMarket;
    }

    public String getPayAccountNo() {
        return payAccountNo;
    }

    public void setPayAccountNo(String payAccountNo) {
        this.payAccountNo = payAccountNo;
    }

    public String getRecvAccountNo() {
        return recvAccountNo;
    }

    public void setRecvAccountNo(String recvAccountNo) {
        this.recvAccountNo = recvAccountNo;
    }

    public String getPayAccountName() {
        return payAccountName;
    }

    public void setPayAccountName(String payAccountName) {
        this.payAccountName = payAccountName;
    }

    public String getRecvAccountName() {
        return recvAccountName;
    }

    public void setRecvAccountName(String recvAccountName) {
        this.recvAccountName = recvAccountName;
    }
}
